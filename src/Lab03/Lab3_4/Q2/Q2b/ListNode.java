package Lab03.Lab3_4.Q2.Q2b;

public class ListNode {
    public Object data;   // set to public for implementation convenience
    public ListNode next;

    public ListNode(Object data) {
        this.data = data;
        this.next = null;
    }

    public ListNode(Object data, ListNode next) {
        this.data = data;
        this.next = next;
    }
}

       		       

